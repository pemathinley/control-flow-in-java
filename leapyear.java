import java.util.*;
public class leapyear
{
	public static void main(String[] x)
	{ 
	int year;
	Scanner sc= new Scanner(System.in);
	year=sc.nextInt();
	if(year%4==0)
	  {
	  System.out.print("leap year");
	  }
	else 
	  {
	  System.out.print("not a leap year");
	  }
	
	
	
	}
	
}	


 
     
	
     